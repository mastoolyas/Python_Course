def add(num1: int, num3: int, num2: int = 0) -> int:
    """
    for example: add(2, 3) -> 5
    :param num1: integer value
    :param num2: integer value
    :return: sum of num1 and num2
    """
    res = num1 + num2 + num3
    return res


def sub(num1: int, num2: int = 0) -> int:
    """
    for example: add(2, 3) -> 5
    :param num1: integer value
    :param num2: integer value
    :return: sum of num1 and num2
    """
    res = num1 - num2
    return res